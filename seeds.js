// function to calculate the reviews average of a specific campground
const calculateAverage = (reviews) => {
    if (reviews.length === 0) {
        return 0;
    }
    var sum = 0;
    reviews.forEach(function (element) {
        sum += element.rating;
    });
    return sum / reviews.length;
}
 
